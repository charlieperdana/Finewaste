import Foundation

enum FinewasteDateFormat: String {
    case simple = "d MMMM yyyy"
    case dayDate = "EEEE, d MMM yyyy"
    case dateAndTime = "EEEE, d MMMM yyyy, h:mm a"
    case militaryTime = "HH:mm"
}
